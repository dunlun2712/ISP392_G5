package Model;

/**
 *
 * @author tranm
 */
public class Account {
    String acc,pass,role,email,status;

    public Account() {
    }

    
    public Account(String acc, String pass, String role, String email, String status) {
        this.acc = acc;
        this.pass = pass;
        this.role = role;
        this.email = email;
        this.status = status;
    }

    public String getAcc() {
        return acc;
    }

    public void setAcc(String acc) {
        this.acc = acc;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
